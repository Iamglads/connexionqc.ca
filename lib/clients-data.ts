// Group clients into rows for the animation
export const groupClientsIntoRows = (clients: Client[]): Client[][] => {
  const itemsPerRow = 8;
  const rows: Client[][] = [];
  
  for (let i = 0; i < clients.length; i += itemsPerRow) {
    rows.push(clients.slice(i, i + itemsPerRow));
  }
  
  // Ensure we have at least 3 rows, pad with empty clients if needed
  while (rows.length < 3) {
    rows.push(Array(itemsPerRow).fill({ name: '', logo: '' }));
  }
  
  return rows;
};